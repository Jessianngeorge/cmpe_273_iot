package ass2_server;

public class registratio_info {
	private String version;
	private String lifetime;
	private String binding_mode;
	private String sms_no;
	private String endpoint_name;
	
	public String getEndpoint_name() {
		return endpoint_name;
	}
	public void setEndpoint_name(String endpoint_name) {
		this.endpoint_name = endpoint_name;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getLifetime() {
		return lifetime;
	}
	public void setLifetime(String lifetime) {
		this.lifetime = lifetime;
	}
	public String getBinding_mode() {
		return binding_mode;
	}
	public void setBinding_mode(String binding_mode) {
		this.binding_mode = binding_mode;
	}
	public String getSms_no() {
		return sms_no;
	}
	public void setSms_no(String sms_no) {
		this.sms_no = sms_no;
	}
	
	
	

}
